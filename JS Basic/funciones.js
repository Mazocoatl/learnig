// Tipod de funciones
// // Función declarada
function miFuncion() {
    return 3;
}
// // Función expresada
var miFuncion = function(a, b) {
    return a + b;
}
// // Función flecha
var miFuncion = (a, b) => a + b;
// // Función flecha con cuerpo
var miFuncion = (a, b) => {
    var c = a + b;
    return c;
}
// // Función flecha con cuerpo y retorno implícito
var miFuncion = (a, b) => a + b;
// // Función flecha con cuerpo y retorno implícito
var miFuncion = a => a + 5;
// // Función flecha con cuerpo y retorno implícito
var miFuncion = () => 3;
// // Función flecha con cuerpo y retorno implícito
var miFuncion = () => {
    return 3;
}
// // Función flecha con cuerpo y retorno implícito
var miFuncion = () => {
    var a = 3;
    return a;
}
// // Función flecha con cuerpo y retorno implícito
var miFuncion = () => {
    var a = 3;
    var b = 5;
    return a + b;
}
// // Función flecha con cuerpo y retorno implícito
var miFuncion = () => {
    var a = 3;
    var b = 5;
    var c = a + b;
    return c;
}  
// // Función flecha con cuerpo y retorno implícito
var miFuncion = () => {
    var a = 3;
    var b = 5;
    var c = a + b;
    var d = c + 5;
    return d;
}

