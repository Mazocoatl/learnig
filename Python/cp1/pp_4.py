#tipos de datos
#strings = cadenas de caracteres (texto)
my_name = "Jair"
print("my_name =>", my_name)

#se utiliza la funcion type para saber que tipo de dato es
print(type(my_name))

#int = numeros enteros
my_age = 30
print("my_age =>", my_age)
print(type(my_age))

#float = numeros de punto flotante (decimales)
costo = 49.99
print("costo =>", costo)
print(type(costo))

#boolean = valores verdadero o falso
single = False
print("single =>", single)
print(type(single))

#input
dato = input("Introduce algun dato (numero o texto)")
print("dato =>",dato)
print(type(dato))